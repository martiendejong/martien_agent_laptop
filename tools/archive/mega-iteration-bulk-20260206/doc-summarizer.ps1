<#
.SYNOPSIS
    Auto-generate summary files for large documentation

.DESCRIPTION
    Creates SUMMARY.md files for documentation that exceeds optimal size.
    Generates executive summaries using AI or structured content extraction.

.PARAMETER FilePath
    Path to file to summarize (if not specified, processes all oversized files)

.PARAMETER OutputSuffix
    Suffix for summary files (default: "_SUMMARY")

.PARAMETER UseAI
    Use AI (Claude API) for intelligent summarization (default: $false)

.PARAMETER MaxSummaryTokens
    Target token count for summaries (default: 2000)

.EXAMPLE
    .\doc-summarizer.ps1 -FilePath "C:\scripts\CLAUDE.md"
    Generate summary for single file

.EXAMPLE
    .\doc-summarizer.ps1 -UseAI $true
    Process all oversized files with AI summarization

.NOTES
    Part of Improvement #4: Smart file size warnings
    Expert: Prof. Emily Bradford (Documentation structure patterns)
#>

param(
    [string]$FilePath = "",
    [string]$OutputSuffix = "_SUMMARY",
    [bool]$UseAI = $false,
    [int]$MaxSummaryTokens = 2000
)

# Extract key sections from markdown content
function Extract-KeySections {
    param([string]$Content)

    $lines = $Content -split "`n"
    $summary = @()
    $inCodeBlock = $false

    foreach ($line in $lines) {
        # Track code blocks
        if ($line -match '^```') {
            $inCodeBlock = -not $inCodeBlock
            continue
        }

        # Skip code blocks
        if ($inCodeBlock) {
            continue
        }

        # Include headers (all levels)
        if ($line -match '^#+\s+(.+)$') {
            $summary += $line
            continue
        }

        # Include key paragraphs (those starting with keywords)
        if ($line -match '^\s*(IMPORTANT|CRITICAL|NOTE|WARNING|MANDATORY|REQUIRED):') {
            $summary += $line
            continue
        }

        # Include bold statements
        if ($line -match '\*\*[^*]+\*\*') {
            $summary += $line
            continue
        }
    }

    return $summary -join "`n"
}

# Generate structured summary
function Generate-StructuredSummary {
    param(
        [string]$FilePath,
        [string]$Content,
        [int]$TargetTokens
    )

    $fileName = [System.IO.Path]::GetFileName($FilePath)
    $keySections = Extract-KeySections -Content $Content

    # Calculate actual size
    $actualTokens = [int]($Content.Length / 4)

    $summary = @"
# Summary: $fileName

**This is an auto-generated summary. For complete details, see: [$fileName](./$fileName)**

---

## Document Overview
- **Full document**: $fileName
- **Approximate size**: $actualTokens tokens
- **Generated**: $(Get-Date -Format "yyyy-MM-dd HH:mm:ss")
- **Summary type**: Structured extraction

---

## Key Sections

$keySections

---

## How to Use This Summary

1. **Quick scan**: Read this summary to understand main topics
2. **Deep dive**: Open full document when you need complete details
3. **Search**: Use Ctrl+F to find specific topics in full document

## Full Document
For complete information, algorithms, examples, and detailed explanations, see:
**→ [$fileName](./$fileName)**

---
*Auto-generated by doc-summarizer.ps1 (Improvement #4)*
*This summary contains approximately $([int]($keySections.Length / 4)) tokens (vs $actualTokens in full document)*
"@

    return $summary
}

